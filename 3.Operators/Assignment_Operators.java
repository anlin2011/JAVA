package HelloWorld;

public class Assignment_Operators {

	public static void main(String[] args) {
		int a = 123;
		System.out.println(a);
		a += 100;
		System.out.println(a);
		a -= 10;
		System.out.println(a);
		a *= 10;
		System.out.println(a);
		a /= 10;
		System.out.println(a);
		a %= 10;
		System.out.println(a);
	}

}
