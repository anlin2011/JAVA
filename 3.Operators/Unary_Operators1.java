package HelloWorld;

public class Unary_Operators1 {

	public static void main(String[] args) {
		int num1 = 10;
		int num2 = -10;
		int num3 = 45;
		int num4 = -32;
		boolean a = true;
		boolean b = false;
		System.out.println(~num1); //10+1 = -11
		System.out.println(~num2); //-10+1 = 9
		System.out.println(~num3); // 45+1 = -46
		System.out.println(~num4); //-32+1 = +31
		System.out.println(!a);
		System.out.println(!b);
	}

}
