package HelloWorld;

public class If_else {

	public static void main(String[] args) {
		int a = 78;
		int b = 34;
		if(a<b) {
			System.out.println("a is less than b");	//Executes when condition is true	
		}
		else {
			System.out.println("a is greater than b"); //Executes when condition is false
		}
}
}